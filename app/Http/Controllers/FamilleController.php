    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // return $request;
        $famille = new Famille(['code' => $request['code'], 'libelle' => $request["libelle"], 'taxe_id' => (intval($request['taxe']) == -1) ? null : intval($request['taxe'])]);
        $famille->save();
        return redirect()->route('ajout_famille');
    }

